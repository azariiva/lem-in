#include "libam.h"

void			am_clear_av(t_am *am)
{
	size_t	i;

	i = -1;
	while (++i < am->size)
	{
		am->r[i]->v = 0;
		ft_bzero((void *)am->a[i], am->size * sizeof(int));
	}
}
