#include "libam.h"

void	am_rmf(t_am *am, size_t v)
{
	size_t	i;

	if (v == 0 || v == am->size - 1)
		return ;
	i = -1;
	while (++i < am->size)
	{
		if (am->f[v][i])
		{
			am->f[v][i] = 0;
			am->f[i][v] = 0;
			am_rmf(am, i);
		}
	}
}
