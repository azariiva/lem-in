#include "libway.h"

size_t	way_count(t_am *am)
{
	size_t	i;
	size_t	ctr;

	ctr = 0;
	i = -1;
	while (++i < am->size)
	{
		if (am->f[0][i] == 1)
			++ctr;
	}
	return (ctr);
}
